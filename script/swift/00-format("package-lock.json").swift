let subpaths = FileManager.default.subpaths(atPath: ".") ?? []

/* A file _has to_ include ~~exactly 1 new line character~~!
*/
let condition: (String) -> Bool = {
	let incrementIfNewline: (Int, Character) -> Int = {
		$1.isNewline ? $0 + 1 : $0
	}
	return $0.reduce(0, incrementIfNewline) == 1
}

execute(for: subpaths, satisfies: condition) { subpath in
	subpath.hasSuffix("package-lock.json")
}
